package pcAula01ex03;

public class Exemplo03 {
	public static void main(String[] args) {
		// Exemplo de tipo inteiro (int)
		int numeroInteiro = 10;
		System.out.println("Exemplo de int: " + numeroInteiro);
		
		//Exemplo de tipo ponto flutuante (double)
		double numeroDecimal = 5.75;
		System.out.println("Exemplo de double " + numeroDecimal);
		
		//Exemplo de tipo byte (valores de -128 a 127)
		byte numeroByte = 127;
		System.out.println("Exemplo de byte: " + numeroByte);
		
		//Exemplo de tipo short (valores de -32.768 a 32.767)
		short numeroShort = 32000;
		System.out.println("Exemplo de short: " + numeroShort);
		
		// Exemplo de tipo long (valores maiores que int)
		long numeroLong = 9223372036854775807L;
		System.out.println("Exemplo de long: " + numeroLong);
		
		// Exemplo de tipo caractere (char)
		char caractere = 'A';
		System.out.println("Exemplo de char: " + caractere);
		
		// Exemplo de tipo de String (sequência de caracteres)
		String texto = "Olá, mundo!";
		System.out.println("Exemplo de String: " + texto);
		
		// Exemplo de tipo boolean (verdadeiro ou falso)
		boolean Verdadeiro = true;
		boolean Falso = false;
		System.out.println("Exemplo de boolean (verdadeiro): " + Verdadeiro);
		System.out.println("Exemplo de boolean (false): " + Falso);
		
		
	}
}
