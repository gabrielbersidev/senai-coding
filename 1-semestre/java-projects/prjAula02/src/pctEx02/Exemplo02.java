package pctEx02;

public class Exemplo02 {
	public static void main(String[] args){
		int a = 2;
		int b = 3;
		
		System.out.println("Operadores Relacionais:");
		System.out.println(a + " é igual a " + b + ":" + " " + (a==b)); // comparação de igualdade
		System.out.println(a + " é diferente de " + b + ":" + " " +	(a!=b)); // comparação de diferença
		System.out.println(a + " é maior que " + b + ":" + " " + (a>b)); // comparação de maior que
		System.out.println(a + " é menor que " + b + ":" + " " + (a<b)); // comparação de menor que
		System.out.println(a + " é maior ou igual a " + b + ":" + " " + (a>=b)); // comparação de maior ou igual a
		System.out.println(a + " é menor ou igual a " + b + ":" + " " + (a<=b)); // comparação de menor ou igual a

	}
}
